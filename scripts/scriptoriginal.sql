@C_INV INT, @NOM VARCHAR(200), @RFC VARCHAR(200), @FECHA_INV DATETIME, @C_COL INT, @C_CALLE INT, @C_DELEG INT, @C_EMP INT, @C_INV_ASES INT
AS
BEGIN
SET DATEFORMAT YMD
INSERT INTO INVENTOR VALUES(@C_INV, @NOM, @RFC, @FECHA_INV, @C_COL, @C_CALLE, @C_DELEG, @C_EMP, @C_INV_ASES)
END

